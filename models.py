class BiDAFCombined(nn.Module):
    """
    BiDAF Combined model which uses character embeddings, data augmentation and our best performing self-attention.
    Args:
        word_vectors (torch.Tensor): Pre-trained word vectors.
        hidden_size (int): Number of features in the hidden state at each layer.
        drop_prob (float): Dropout probability.
    """
    def __init__(self, word_vectors, char_vectors, hidden_size, drop_prob=0., char_drop_prob = 0., use_char_emb=True):
        super(BiDAFCombined, self).__init__()
        self.use_char_emb = use_char_emb
        self.char_drop_prob = char_drop_prob

        self.emb = layers.EmbeddingWithChar(word_vectors=word_vectors,
                                    char_vectors=char_vectors,
                                    hidden_size=hidden_size,
                                    drop_prob=drop_prob,
                                    char_drop_prob=char_drop_prob,
                                    use_char_emb=use_char_emb)

        self.enc = layers.RNNEncoder(input_size=hidden_size,
                                     hidden_size=hidden_size,
                                     num_layers=1,
                                     drop_prob=drop_prob)

        self.att = layers.BiDAFAttention(hidden_size=2 * hidden_size,
                                         drop_prob=drop_prob)
        
        # Add self attention as well as BIDAF attention
        
        self.att2 = layers.SelfAttention(input_dim=8 * hidden_size,
                                        hidden_size=4 * hidden_size,
                                        drop_prob=drop_prob) # may want to choose to update these params

        self.mod = layers.RNNEncoder(input_size=8 * hidden_size,
                                     hidden_size=hidden_size,
                                     num_layers=2,
                                     drop_prob=drop_prob)

        self.out = layers.BiDAFOutput(hidden_size=hidden_size,
                                      drop_prob=drop_prob)

    def forward(self, cw_idxs, cc_idxs, qw_idxs, qc_idxs):
        c_mask = torch.zeros_like(cw_idxs) != cw_idxs
        q_mask = torch.zeros_like(qw_idxs) != qw_idxs
        c_len, q_len = c_mask.sum(-1), q_mask.sum(-1)

        if self.use_char_emb:
            c_emb = self.emb(cw_idxs, cc_idxs)
            q_emb = self.emb(qw_idxs, qc_idxs)
        else:
            c_emb = self.emb(cw_idxs, None)
            q_emb = self.emb(qw_idxs, None)
        
        c_enc = self.enc(c_emb, c_len)    # (batch_size, c_len, 2 * hidden_size)
        q_enc = self.enc(q_emb, q_len)    # (batch_size, q_len, 2 * hidden_size)
        att = self.att(c_enc, q_enc,
                       c_mask, q_mask)    # (batch_size, c_len, 8 * hidden_size)
        att2 = self.att2(att)
        mod = self.mod(att2, c_len)        # (batch_size, c_len, 2 * hidden_size)

        out = self.out(att, mod, c_mask)  # 2 tensors, each (batch_size, c_len)

        return out

